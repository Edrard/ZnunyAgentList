test_write_lifecycle() {
    section 'Optional controlled write lifecycle'
    if [ "$RUN_WRITE_TESTS" != "yes" ]; then
        skip 'Write lifecycle tests skipped because RUN_WRITE_TESTS is not yes.'
        return
    fi

    warn 'RUN_WRITE_TESTS=yes: this section performs real ticket writes.'

    local article_body close_body reopen_body
    article_body="{\"TicketID\":\"$(json_escape "$TEST_TICKET_ID")\",\"Kind\":\"internal_note\",\"Subject\":\"ZnunyAgentList smoke test article\",\"Body\":\"Repository smoke test internal note.\"}"
    close_body="{\"TicketID\":\"$(json_escape "$TEST_TICKET_ID")\",\"Kind\":\"internal_note\",\"Subject\":\"ZnunyAgentList smoke test close\",\"Body\":\"Repository smoke test closing ticket.\",\"Reason\":\"ZnunyAgentList smoke test close\"}"
    reopen_body="{\"TicketID\":\"$(json_escape "$TEST_TICKET_ID")\",\"Kind\":\"internal_note\",\"Subject\":\"ZnunyAgentList smoke test reopen\",\"Body\":\"Repository smoke test reopening ticket.\",\"Reason\":\"ZnunyAgentList smoke test reopen\"}"

    request POST /TicketArticle 200 "$article_body" yes no
    request POST /TicketClose 200 "$close_body" yes no
    request GET "/ZnunyAgentListTicket/$(urlencode "$TEST_TICKET_ID")" 200 "" yes no
    expect_body_regex '"StateType"[[:space:]]*:[[:space:]]*"closed"' 'Ticket is closed after TicketClose.'
    request POST /TicketReopen 200 "$reopen_body" yes no
    request GET "/ZnunyAgentListTicket/$(urlencode "$TEST_TICKET_ID")" 200 "" yes no
    expect_body_regex '"StateType"[[:space:]]*:[[:space:]]*"open"' 'Ticket is open after TicketReopen.'
}

main() {
    collect_config
    init_log

    test_health
    test_session
    test_agents_queues
    test_customer_users
    test_dictionaries
    test_defaults_validation
    test_safe_tickets
    test_generic_ticket_connector
    test_write_lifecycle

    section 'Summary'
    log_line "PASS: $PASS"
    log_line "WARN: $WARN"
    log_line "FAIL: $FAIL"
    log_line "SKIP: $SKIP"

    if [ "$FAIL" -eq 0 ]; then
        log_line 'RESULT: OK'
        exit 0
    fi

    log_line 'RESULT: FAILED'
    exit 1
}

main "$@"

